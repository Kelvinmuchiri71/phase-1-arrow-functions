//arrow function code

//declare function expression called divide
const divide = function() {
    return 2000 / 100;
}

//declare an arrow function called square
const square = (k) => k * k;

//declare an arrow function called add
const add = (a, b) => a + b; 


console.log(divide());     //output 200
console.log(square(8));    //output 64
console.log(add(30, 20));  //output 50
